int isEqual(int x, int y) {
  //x ^ y should be 0 if they're equal
  int p = x ^ y;
  return !(p);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  //change x into either 1 or 0
  x = !(!x);
  //construct the int for either 0xFFFFFFFF or 0x00000000
  x |= x << 1;
  x |= x << 2;
  x |= x << 4;
  x |= x << 8;
  x |= x << 16;
  //if x = 1, then it should be 0xFFFFFFFF now, thus it will return y
  //else it will return z because it is 0x00000000
  return ( x & y ) | ( ~x & z );
}
/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
  int placeToReplace = n << 3;
  //clear the byte that should be replaced
  int placeCleaner = 0xff << placeToReplace;
  //replace the cleared byte with an | operation
  x = (~placeCleaner&x) | c << placeToReplace;
  return x;
}
/*
 * isTmax - returns 1 if x is the maximum, two's complement number,
 *     and 0 otherwise 
 *   Legal ops: ! ~ & ^ | +
 *   Max ops: 10
 *   Rating: 1
 */
int isTmax(int x) {
  //Tmax = 0x800000000, to construct a Tmax
  int comparator = ~x;
  int constant = 0x80;
  constant = constant << 24;
  return !(comparator ^ constant);
}
/* 
 * negate - return -x 
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) {
  //~0 = -1, ~-1 = 0, thus it requires a + 1
  signed int result = ~x+1;
  return result;
}
/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int divpwr2(int x, int n) {
    //adder to correct the division for the negative numbers
    int musk = (1 << n) + ~0;
    int adder = (x >> 31) & musk; 
    // For two's complement wrong code
    // x = (~x) + musk;
    // x = x >> n;
    // x = (~x) + musk;
    return (x + adder) >> n;
}
